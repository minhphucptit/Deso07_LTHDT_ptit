    private void jComboBox5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox5ActionPerformed
        // TODO add your handling code here:
        int i=jComboBox5.getSelectedIndex();
        if(i==0){
            Collections.sort(list3, new Comparator<BangDiem>() {
                @Override
                public int compare(BangDiem d1, BangDiem d2) {
                    String ten1=d1.getSv().getTen();
                    String ten2=d2.getSv().getTen();
                    String s1=ten1.substring(ten1.lastIndexOf(" ")+1)+ten1;
                    String s2=ten2.substring(ten2.lastIndexOf(" ")+1)+ten2;
                    return s1.compareToIgnoreCase(s2);
                }
            });
            showTable3();
        }else if(i==1){
            Collections.sort(list3, new Comparator<BangDiem>() {
                @Override
                public int compare(BangDiem d1, BangDiem d2) {
                    String ten1=d1.getMh().getTen();
                    String ten2=d2.getMh().getTen();
                    String s1=ten1.substring(ten1.lastIndexOf(" ")+1)+ten1;
                    String s2=ten2.substring(ten2.lastIndexOf(" ")+1)+ten2;
                    return s1.compareToIgnoreCase(s2);
                }
            });
            showTable3();
        } 
    }//GEN-LAST:event_jComboBox5ActionPerformed


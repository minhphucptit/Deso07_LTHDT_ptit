    public void showTable4(int ma){
       if(list3.size()>0){
           float tong=0;
           int tinchi=0;
            model4.setRowCount(0);
            for(int i=0;i<list3.size();i++){
                BangDiem bd=list3.get(i);
                if(bd.getSv().getMa()==ma){
                    tong=tong+bd.getDiem() * bd.getMh().getSoDv();
                    tinchi=tinchi+bd.getMh().getSoDv();
                    model4.addRow(list3.get(i).toObject());
                }
            }
            jTable4.setModel(model4);
            float tb=0;
            try{
                tb=tong/tinchi;
                jLabel14.setText(Float.toString(tb));
            }catch(Exception e){
                jLabel14.setText(Float.toString(tb));
            }
        } 
    }
